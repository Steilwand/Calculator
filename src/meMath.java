public class meMath {

    public double plus(double Param1, double Param2){

        double ergebnis = Param1 + Param2;
        return ergebnis;
    }

    public double minus(double Param1, double Param2){

        double ergebnis = Param1 - Param2;
        return ergebnis;
    }

    public double mal(double Param1, double Param2){

        double ergebnis = Param1 * Param2;
        return ergebnis;
    }

    public double geteilt(double Param1, double Param2){

        double ergebnis = Param1 / Param2;
        return ergebnis;
    }


}
